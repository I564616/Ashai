package com.sabmiller.staff.storefront.form;

/**
 * Form for Customer Search page.
 *
 * @author yuxiao.wang
 *
 */
public class SABMCustomerSearchForm
{
	private String accountPayerNumber;
	private String customerName;
	private String address;
	private String suburb;
	private String expiryDateMonth;
	private String postcode;
	private String email;

	/**
	 * @return the accountPayerNumber
	 */
	public String getAccountPayerNumber()
	{
		return accountPayerNumber;
	}

	/**
	 * @param accountPayerNumber
	 *           the accountPayerNumber to set
	 */
	public void setAccountPayerNumber(final String accountPayerNumber)
	{
		this.accountPayerNumber = accountPayerNumber;
	}

	/**
	 * @return the customerName
	 */
	public String getCustomerName()
	{
		return customerName;
	}

	/**
	 * @param customerName
	 *           the customerName to set
	 */
	public void setCustomerName(final String customerName)
	{
		this.customerName = customerName;
	}

	/**
	 * @return the address
	 */
	public String getAddress()
	{
		return address;
	}

	/**
	 * @param address
	 *           the address to set
	 */
	public void setAddress(final String address)
	{
		this.address = address;
	}

	/**
	 * @return the suburb
	 */
	public String getSuburb()
	{
		return suburb;
	}

	/**
	 * @param suburb
	 *           the suburb to set
	 */
	public void setSuburb(final String suburb)
	{
		this.suburb = suburb;
	}

	/**
	 * @return the expiryDateMonth
	 */
	public String getExpiryDateMonth()
	{
		return expiryDateMonth;
	}

	/**
	 * @param expiryDateMonth
	 *           the expiryDateMonth to set
	 */
	public void setExpiryDateMonth(final String expiryDateMonth)
	{
		this.expiryDateMonth = expiryDateMonth;
	}

	/**
	 * @return the postcode
	 */
	public String getPostcode()
	{
		return postcode;
	}

	/**
	 * @param postcode
	 *           the postcode to set
	 */
	public void setPostcode(final String postcode)
	{
		this.postcode = postcode;
	}

	/**
	 * @return the email
	 */
	public String getEmail()
	{
		return email;
	}

	/**
	 * @param email
	 *           the email to set
	 */
	public void setEmail(final String email)
	{
		this.email = email;
	}



}
